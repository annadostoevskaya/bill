inline F32 f32Cos(F32 x)
{
    return cosf(x);
}

inline F32 f32Sin(F32 x)
{
    return sinf(x);
}

inline F32 f32ArcCos(F32 x)
{
    return acosf(x);
}

inline F32 f32ArcSin(F32 x)
{
    return asinf(x);
}

